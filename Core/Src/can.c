void CAN_Reconfigure_And_Start(void)
{
	HAL_CAN_Stop(&hcan);
    // --- Налаштування фільтрів ---
	CAN_FilterTypeDef sFilterConfig;

	sFilterConfig.FilterBank = 0;
	sFilterConfig.FilterMode = CAN_FILTERMODE_IDMASK;
	sFilterConfig.FilterScale = CAN_FILTERSCALE_32BIT;
	sFilterConfig.FilterIdHigh = 0x0000;
	sFilterConfig.FilterIdLow = 0x0000;
	sFilterConfig.FilterMaskIdHigh = 0x0000;
	sFilterConfig.FilterMaskIdLow = 0x0000;
	sFilterConfig.FilterFIFOAssignment = CAN_FILTER_FIFO0;
	sFilterConfig.FilterActivation = CAN_FILTER_ENABLE;
	sFilterConfig.SlaveStartFilterBank = 14;

	if (HAL_CAN_ConfigFilter(&hcan, &sFilterConfig) != HAL_OK) {
		Error_Handler();
	}
	// --- Запуск CAN контролера ---
	if (HAL_CAN_Start(&hcan) != HAL_OK)
	{
		printf("CAN Start Failed!\r\n");
		Error_Handler();
	}
    CAN_Start_Interrupt(&hcan);

    printf("CAN (Re)Started Successfully!\r\n");
}

void CAN_Send(uint16_t nodeId,uint8_t *data,uint8_t size)
{
    CAN_TxHeaderTypeDef TxHeader;

    TxHeader.StdId = nodeId;
    TxHeader.IDE= CAN_ID_STD;
    TxHeader.RTR = CAN_RTR_DATA;
    TxHeader.DLC = size;
    TxHeader.ExtId = 0;
    TxHeader.TransmitGlobalTime = DISABLE;
    uint32_t mailbox;

    // 3. Перевірка, чи є місце у черзі на відправку
    if (HAL_CAN_GetTxMailboxesFreeLevel(&hcan) > 0)
    {
        if (HAL_CAN_AddTxMessage(&hcan, &TxHeader, data, &mailbox) != HAL_OK)
        {
            can_errtx_count++;
        }
    } else
    {
        // Черга повна, повідомлення не буде відправлено
    	can_errtx_count++;
		printf("can tx error! %i\n",can_errtx_count);
		CAN_Reconfigure_And_Start();
    }
}

