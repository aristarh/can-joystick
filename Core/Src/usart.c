void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef *huart, uint16_t Size)
{
	if (huart->Instance == USART1)
	{
		uart_rcv(huart,Size);
		// Перезапускаємо прийом даних через DMA
		uart1_interrupt();
	}
}

void uart1_interrupt()
{
	HAL_UARTEx_ReceiveToIdle_DMA(&huart1, dma_rx_buf, DMA_BUF_SIZE);
	__HAL_DMA_DISABLE_IT(&hdma_usart1_rx, DMA_IT_HT);
	__HAL_DMA_DISABLE_IT(&hdma_usart1_rx, DMA_IT_TC);
}

void uart_rcv(UART_HandleTypeDef *huart,uint16_t pos)
{

	static uint16_t dma_uart_rx = 0;
	uint16_t len;
	if (pos > dma_uart_rx)
	{
		len = pos - dma_uart_rx;
	}
	else
	{
		len = (DMA_BUF_SIZE - dma_uart_rx) + pos;
	}

	if (len > 0 && len < DATA_BUFFER_SIZE)
	{
		if (pos > dma_uart_rx)
		{
			memcpy(uart_data, &dma_rx_buf[dma_uart_rx], len);
		}
		else
		{
			uint16_t first_part_len = DMA_BUF_SIZE - dma_uart_rx;
			memcpy(uart_data, &dma_rx_buf[dma_uart_rx], first_part_len);

			memcpy(&uart_data[first_part_len], dma_rx_buf, pos);
		}
	}
	dma_uart_rx = pos;
	uart_process(len);

}

void uart_process(uint8_t len)
{
	if (huart1.gState !=HAL_UART_STATE_READY)  return;
	CAN_Send(1, uart_data, 8);
	HAL_UART_Transmit(&huart1, (uint8_t *)uart_data, len, 0x0F);
}
